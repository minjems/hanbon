// Fallback: decompose and use chosung/jungsung base mappings
const CHOSUNG_PINYIN = ['g','g','n','d','d','l','m','b','b','s','s','','z','z','ch','k','t','p','h'];
const JUNGSUNG_PINYIN = ['a','ai','ya','yai','o','ei','yo','yei','o','wa','wai','wei','yo','u','wo','wei','wi','yu','u','yi','i'];
const JONGSUNG_PINYIN = ['','k','k','k','n','n','n','t','l','k','m','b','t','t','b','l','m','b','b','t','t','ng','t','t','k','t','b','t'];

export function convert(text) {
    let result = '';
    for (const char of text) {
        if (char === ' ') {
            result += '-';
        } else if (isKoreanSyllable(char)) {
            // First try exact syllable lookup
            if (SYLLABLE_MAP[char]) {
                result += SYLLABLE_MAP[char];
                continue;
            }

            // Try the character with jongsung stripped, then add jongsung separately
            const { initial, medial, final: f } = decompose(char);
            const baseChar = compose(initial, medial, 0);

            if (f > 0 && SYLLABLE_MAP[baseChar]) {
                result += SYLLABLE_MAP[baseChar];
                // For jongsung, try to find a simple character
                result += JONGSUNG_PINYIN[f];
            } else if (SYLLABLE_MAP[baseChar]) {
                result += SYLLABLE_MAP[baseChar];
            } else {
                // Ultimate fallback: pinyin-style
                result += CHOSUNG_PINYIN[initial] + JUNGSUNG_PINYIN[medial] + JONGSUNG_PINYIN[f];
            }
        } else if (isKoreanConsonant(char)) {
            const idx = getChosungIndex(char);
            if (idx >= 0) result += CHOSUNG_PINYIN[idx];
        } else if (isKoreanVowel(char)) {
            const idx = getJungsungIndex(char);
            if (idx >= 0) result += JUNGSUNG_PINYIN[idx];
        } else {
            result += char;
        }
    }
    return result;
}

