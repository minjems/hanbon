export function convert(text) {
    let result = '';
    for (const char of text) {
        if (char === ' ') {
            result += '-';
        } else if (isKoreanSyllable(char)) {
            const { initial, medial, final: f } = decompose(char);

            // Build the base syllable (without jongsung) for lookup
            const baseCode = 0xAC00 + (initial * 588) + (medial * 28);
            const baseChar = String.fromCharCode(baseCode);

            const kana = SYLLABLE_MAP[baseChar];
            if (kana) {
                result += kana;
            } else {
                // Fallback: output original character
                result += baseChar;
            }

            // Add jongsung
            result += JONGSUNG_TO[f];
        } else if (isKoreanConsonant(char)) {
            result += CONSONANT_MAP[char] || '';
        } else if (isKoreanVowel(char)) {
            result += VOWEL_MAP[char] || '';
        } else {
            result += char;
        }
    }
    return result;
}

